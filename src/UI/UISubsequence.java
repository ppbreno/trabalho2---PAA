    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int m = Integer.parseInt(jTextField1.getText());
        int n = Integer.parseInt(jTextField2.getText());
        model1 = (DefaultTableModel) jTable1.getModel();
        model2 = (DefaultTableModel) jTable2.getModel();
        int[][] mat = new int[m+1][n+1];
        String[] c1 = new String[m];
        String[] c2 = new String[n];
        
        for(int k = 0; k < m; k++){
            c1[k] = model1.getValueAt(0, k).toString();
        }
        for(int k = 0; k < n; k++){
            c2[k] = model2.getValueAt(0, k).toString();
        }
        
        for(int i = 0; i <= m; i++){
            for (int j = 0; j <= n; j++){
                if (i == 0 || j == 0) mat[i][j] = 0;
                else if (c1[i - 1].equals(c2[j - 1])) mat[i][j] = mat[i - 1][j - 1] + 1;
                else mat[i][j] = Math.max(mat[i - 1][j], mat[i][j - 1]);
            }   
        }
        
        int indice = mat[m][n];
        String[] seq = new String[indice];

        int i = m, j = n;
        while(i > 0 && j > 0){
            if(c1[i - 1].equals(c2[j - 1])){
                seq[indice - 1] = c1[i - 1];
                i--;
                j--;
                indice--;
            }else if(mat[i - 1][j] > mat[i][j - 1]) i--;
            else j--;
        }
        
        Object[] lcs = seq;
        
        model1 = new DefaultTableModel(0, lcs.length);
        model1.addRow(lcs);
        jTable4.setModel(model1);

        jLabel5.setText(String.valueOf(lcs.length));
    }//GEN-LAST:event_jButton2ActionPerformed


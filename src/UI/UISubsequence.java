    private int[] subsequence (int[] A, int n) {
        int[] c = new int[n];
        for(int m = 1; m < n; n++){
            c[m] = 1;
            for(int i = m-1; i >= 0; i--){
                if(A[i] <= A[m] && c[i]+1 > c[m]) c[m] = c[i] + 1;
            }
        }
        return c;
    }
